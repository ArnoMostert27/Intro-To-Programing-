// 1. EVEN NUMBERS (using += 2)
// This loop prints all even numbers from 0 up to 10
for (var i = 0; i <= 10; i += 2) {

  // Output the current even number to the console
  console.log(i);
}

// 2. ODD NUMBERS (using += 2)
// This loop prints all odd numbers from 1 up to 10
for (var i = 1; i <= 10; i += 2) {

  // Output the current odd number to the console
  console.log(i);
}

// 3. COUNTING DOWN BY 10 (using -= 10)
// This loop starts at 100 and counts down to 0 in steps of 10
for (var i = 100; i >= 0; i -= 10) {

  // Output the current value of i to the console
  console.log(i);
}

