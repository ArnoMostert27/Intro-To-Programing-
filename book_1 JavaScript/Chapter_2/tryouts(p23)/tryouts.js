// Starting number of balloons
var balloons = 100;

// Multiply and assign
balloons *= 2;   // same as: balloons = balloons * 2
console.log(balloons); // 200

// Divide and assign
balloons /= 4;   // same as: balloons = balloons / 4
console.log(balloons); // 50

// Add and assign
balloons += 10;  // same as: balloons = balloons + 10
console.log(balloons); // 60

// Subtract and assign
balloons -= 20;  // same as: balloons = balloons - 20
console.log(balloons); // 40
