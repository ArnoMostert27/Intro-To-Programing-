// Store a name in the variable
var name = "Arno";

// Check if the name matches your own name
if (name === "Arno") {
    console.log("Hello me!"); // Runs if the name is Arno
}
// Check if the name matches your dad's name
else if (name === "DadName") {
    console.log("Hi dad!"); // Runs if the name is your dad
}
// Check if the name matches your mom's name
else if (name === "MomName") {
    console.log("Hi mom!"); // Runs if the name is your mom
}
// Runs if the name matches none of the above
else {
    console.log("Hello stranger!"); // Default message
}
